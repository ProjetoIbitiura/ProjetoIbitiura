    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        char[] _senhaBanco = _pswSenhaNova.getPassword();  
        senha_nova = new String(_senhaBanco);
        char[] _senhaBanco2 = _pswRepetirSenhaNova.getPassword();  
        senha_nova_repetir = new String(_senhaBanco2);
        
        if(!senha_antiga.equals(senha_nova) && senha_nova.equals(senha_nova_repetir))
        {
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/patrimonio", "root", "admin");
                sql = "UPDATE login SET senha_login = '"+ senha_nova +"' WHERE nome_login = '"+nome_Login+"';";
                stmt = con.prepareStatement(sql);
                stmt.execute();
                con.close();
                if(control == 0)
                {
                    dispose();
                    Principal principal = new Principal(nome_Login, senha_nova);
                    principal.setVisible(true);
                }
                else
                {
                    dispose();
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(AlterarSenha.class.getName()).log(Level.SEVERE, null, ex);
            }        
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Senha Invalida!");
            _pswSenhaNova.setText("");
            _pswRepetirSenhaNova.setText("");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

