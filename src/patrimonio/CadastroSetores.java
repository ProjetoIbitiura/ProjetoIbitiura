    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        Statement stm;
        Conexao nova = new Conexao();
        conexao = nova.RetornaConexao();
        try {
            stm = conexao.createStatement();
            SQL = "SELECT * FROM setor;";
            ResultSet rs = stm.executeQuery(SQL);
            while(rs.next())  
            {    
                id_setor = rs.getInt("id_setor");
                nome_setor = rs.getString("nome_setor");
                if(nome_setor.equals(txtNomeSetor.getText()))
                {
                    JOptionPane.showMessageDialog(this, "SETOR JA EXISTE NO BANCO DE DADOS");
                    dispose();
                }
            }
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(CadastroSetores.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(!txtNomeSetor.equals(nome_setor))
        {
            try {
                conexao = nova.RetornaConexao();
                SQL = "insert into setor (id_setor,nome_setor)values(null,'" + txtNomeSetor.getText() + "')";
                stmt = conexao.prepareStatement(SQL);
                stmt.execute();
                conexao.close();
                JOptionPane.showMessageDialog(this, "NOVO SETOR CADASTRADO COM SUCESSO");
                dispose();
            } catch (SQLException ex) {
                Logger.getLogger(CadastroSetores.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "SETOR J√Å CADASTRADO \n INFORME OUTRO NOME");
        }
    }//GEN-LAST:event_btnOkActionPerformed


